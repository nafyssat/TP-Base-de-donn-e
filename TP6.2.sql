
-- suppression des tables précédentes

DROP TABLE IF EXISTS sortie;
DROP TABLE IF EXISTS entree;
DROP TABLE IF EXISTS transfert;
DROP TABLE IF EXISTS bloc;
DROP TABLE IF EXISTS adresse;
DROP TABLE IF EXISTS personne;
-- création des tables

CREATE TABLE personne (
  nom varchar(30), naissance date,
  PRIMARY KEY (nom)
); 

CREATE TABLE adresse (
  adresseid varchar(6), nom varchar(30), soldeinitial int,
  PRIMARY KEY (adresseid),
  FOREIGN KEY (nom) REFERENCES personne(nom)
);

CREATE TABLE bloc (
  blocid int, bloctime date,
  PRIMARY KEY (blocid)
); 

CREATE TABLE transfert (
  txid int, blocid int,
  PRIMARY KEY (txid),
  FOREIGN KEY (blocid) REFERENCES bloc(blocid)
);

CREATE TABLE entree (
  txid int, adresseid varchar(6), montant int,
  PRIMARY KEY (txid,adresseid),
  FOREIGN KEY (txid) REFERENCES transfert(txid),
  FOREIGN KEY (adresseid) REFERENCES adresse(adresseid)
); 

CREATE TABLE sortie (
  txid int, adresseid varchar(6), montant int,
  PRIMARY KEY (txid,adresseid),
  FOREIGN KEY (txid) REFERENCES transfert(txid),
  FOREIGN KEY (adresseid) REFERENCES adresse(adresseid)
); 

